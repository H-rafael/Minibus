                    <p><strong>01</strong><br />
                        <br />

                        曾经，我以为自己是个很幽默的人，比如逗女孩开心，讲的是这样的笑话。<br />
                        <br />
                        一只蚂蚁第一次出门觅食，突然下起了雨，它不知道怎么回到蚁窝，这时遇见了另外一只蚂蚁，于是问：“你都如何回蚁窝？” <br />
                        <br />
                        另一只蚂蚁答：“带……带着笑或是很沉默？ ”<br />
                        <br />
                        后来，我真的听了很多遍《后来》，初听不知曲中意，再听已是曲中人。刘若英唱得淡淡的，却情深意切。<br />
                        <br />
                        遗失一些曾经的美好，经历几段感情的波折，才明白人生真就是一部肥皂剧，狗血的桥段随时在你身边上演，却都不是happy ending。<br />
                        <br />
                        多年以前，我觉得《后来》最戳心的歌词是“你都如何回忆我，带着笑或是很沉默”，而现在，反倒更关注“这些年来，有没有人能让你不寂寞”这一句。<br />
                        <br />
                        也许经历这样一个过程，才是真正的放下了。我最多只想知道过得你好不好，却不再去幻想我们之间是否还有别的可能。哪怕你睡醒后看到的人不再是我，我也会因为你可以拥有幸福而开心。<br />
                        <br />
                        “后来，终于在眼泪中明白，有些人一旦错过就不再。”<br />
                        <br />
